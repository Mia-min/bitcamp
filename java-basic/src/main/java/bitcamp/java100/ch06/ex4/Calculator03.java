package bitcamp.java100.ch06.ex4;

public class Calculator03 {

	int result;

	void plus(int value) {
		this.result += value;
	}

	void minus(int value) {
		this.result -= value;
	}

	void multi(int value) {
		this.result *= value;
	}

	void devide(int value) {
		this.result /= value;
	}
}
