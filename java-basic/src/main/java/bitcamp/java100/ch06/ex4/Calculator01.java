package bitcamp.java100.ch06.ex4;

public class Calculator01 {

	static int result;

	static void plus(int value) {
		result += value;
	}

	static void minus(int value) {
		result -= value;
	}

	static void multi(int value) {
		result *= value;
	}

	static void devide(int value) {
		result /= value;
	}
}
